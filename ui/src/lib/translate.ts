export function translate (s: keyof typeof translations) {
  const available = (translations[s] ?? {}) as Record<string, string>;
  for (const lang of navigator.languages) {
    const firstPart = lang.split("-")[0];
    if (firstPart === "en") {
      return s;
    }
    const t = available[firstPart];
    if (t) {
      return t;
    }
  }
  return s;
}
